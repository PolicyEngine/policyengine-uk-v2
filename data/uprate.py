@lru_cache(maxsize=32)
def _yaml_growth_factors(year: int) -> dict[str, float]:
    """Read growth_factors from the parameter YAML for the transition into `year`.

    The YAML for fiscal year Y/Y+1 carries the OBR rates for the transition
    *into* year Y, so year=2026 reads 2026_27.yaml. This matches the fallback
    `_YOY` table, which keys rates on the entered fiscal year.
    Returns an empty dict when the file is missing or has no growth_factors block.
    """
    fname = f"{year}_{(year + 1) % 100:02d}.yaml"
    path = PARAMS_DIR / fname
    if not path.exists():
        return {}
    with path.open() as f:
        data = yaml.safe_load(f)
    return data.get("growth_factors") or {}


# Index name → growth_factors YAML key.
_YAML_KEY: dict[str, str] = {
    "earnings": "earnings_growth",
    "cpi": "cpi_rate",
    "gdp_pc": "gdp_pc_growth",
    "mixed_pc": "mixed_income_pc_growth",
    "interest": "savings_interest_growth",
    "rent": "rent_growth",
    "mortgage_interest": "mortgage_interest_growth",
    "population": "population_growth",
    "council_tax": "council_tax_growth",
}


def _rate_for(year: int, index: str) -> float:
    """Year-on-year rate for `index` entering fiscal year `year`.

    Prefers the YAML growth_factors value; falls back to the hardcoded table
    (then the default rate) when the YAML or key is unavailable.
    """
    if index == "social_rent":
        rates = dict(_YOY["social_rent"])
        if year in rates:
            return rates[year]
        # Beyond the settlement table: CPI + 1ppt (forecast approximation,
        # fiscal-year CPI standing in for September CPI).
        return _rate_for(year, "cpi") + 0.01
    gf = _yaml_growth_factors(year)
    rate = gf.get(_YAML_KEY[index], 0.0)
    if rate != 0.0:
        return rate
    if index not in _YOY:
        raise KeyError(
            f"no growth_factors YAML value for index {index!r} entering "
            f"{year} and no fallback table"
        )
    rates = dict(_YOY[index])
    return rates.get(year, _DEFAULT_RATE[index])


def cumulative_factor(base_year: int, target_year: int, index: str) -> float:
    """Cumulative growth factor from base_year to target_year for an index.

    Reads rates from parameter YAML growth_factors blocks; falls back to the
    hardcoded table for missing years/indices.
    """
    if target_year == base_year:
        return 1.0
    factor = 1.0
    if target_year > base_year:
        for y in range(base_year + 1, target_year + 1):
            factor *= 1.0 + _rate_for(y, index)
    else:
        for y in range(target_year + 1, base_year + 1):
            factor /= 1.0 + _rate_for(y, index)
    return factor


