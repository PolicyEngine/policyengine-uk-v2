"""Uprate clean FRS microdata between years (either direction).

Python port of the Rust `Dataset::uprate_to_dir` (src/data/mod.rs): applies
year-on-year growth indices to monetary columns and the population index to
weights. Rates come from the `growth_factors` block of each parameter YAML
(generated by data/gen_growth_factors.py from the OBR EFO workbook and MHCLG
council tax tables), with a hardcoded fallback table for years/indices missing
from the YAMLs.

Used for pooling adjacent FRS years onto a common price level, and for shifting
the fixed EFRS panel to each target year (de-uprating for years before the
base). Operates on the clean persons/households CSV columns; benunits carry no
monetary fields and are passed through unchanged.
"""

